CREATE TABLE bs_user(
	id INTEGER(11) PRIMARY KEY AUTO_INCREMENT,
	username VARCHAR(100) NOT NULL UNIQUE,
	PASSWORD VARCHAR(100) NOT NULL,
	email VARCHAR(100)
);
